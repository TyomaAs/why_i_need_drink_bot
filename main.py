# --- drink (з reply) ---
async def drink(update: Update, context: ContextTypes.DEFAULT_TYPE):
    if not update.message:
        return

    now = time.time()
    message_time = update.message.date.timestamp()

    if now - message_time > TTL_SECONDS:
        return

    user_id = update.message.from_user.id

    request_times = user_request_times.get(user_id, deque())
    while request_times and now - request_times[0] > 60:
        request_times.popleft()
    
    request_times.append(now)
    user_request_times[user_id] = request_times

    if len(request_times) >= MAX_REQUESTS_PER_MINUTE:
        return
    elif len(request_times) >= MAX_REQUESTS_PER_MINUTE_WARNING:
        await reply(update, "Сонце, ты зараз забагато запитів кидаєш 🙂 Зачекай хвилинку.")
        return
        
    last_drink = user_last_drink_time.get(user_id, 0)
    elapsed = now - last_drink

    if elapsed < DRINK_INTERVAL:
        remaining = int(DRINK_INTERVAL - elapsed)
        await reply(update, f"Ще рано давати привід упити, зачекай {remaining} секунд ⏳")
        return

    user_last_drink_time[user_id] = now

    reason = random.choice(DRINK_REASONS)
    await reply(update, reason)
    # await reply(update, "У вас є Діма, просіт го")

def main():
    API_TOKEN = os.getenv("API_TOKEN")
    if not API_TOKEN:
        raise ValueError("API_TOKEN environment variable is not set")

    app = ApplicationBuilder().token(API_TOKEN).build()

    # handlers
    app.add_handler(CommandHandler("start", start))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))
    app.add_handler(CommandHandler("drink", handle_drink))
    app.add_handler(CommandHandler("joke", handle_joke))
    app.add_handler(CommandHandler("pupsik", handle_pupsik))
    app.post_init = on_startup

    print("Няньо май починає пити...")
    app.run_polling(drop_pending_updates=True)

if __name__ == "__main__":
    main()
