async def handle_misc(update: Update, text: str, user):
    if text in ["иу", "уи"]:
        nickname = f"@{user.username}" if user.username else user.first_name
        await reply(update, f"иу {nickname}")
        return True

    if any(word in text for word in ["тересва"]):
        await reply(update, "#теребля")
        return True

    if text == "@marjanchick":
        await reply(update, "О, @marjanchick! Не впізнав? Тямиш як ми за гаражами напилися до чортенят? :)")
        return True

    if any(word in text for word in ["закод", "закодірував", "закодував"]):
        await reply(update, "Яке ся я закодірував, бутилка домашнього самогона, вто уто шо гія ми.")
        return True

    return False

# --- main message handler ---
async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    if not update.message or not update.message.text:
        return

    now = time.time()
    message_time = update.message.date.timestamp()

    # TTL
    if now - message_time > TTL_SECONDS:
        return

    text = update.message.text.strip().lower()
    user = update.message.from_user

    # misc (унікальні відповіді, які повністю перебивають інше)
    if await handle_misc(update, text, user):
        return

    # if any(word in text for word in FCK_WORDS):
    #     await reply(update, "аяяяй, ты ся не ганьбиш?")

    # кнопки (ключ до стабільності)
    if "дай привід" in text:
        await handle_drink(update, context)
        return

    if "алкаш-пупсик" in text:
        await handle_pupsik(update, context)
        return

    # тригери
    if any(word in text for word in TRIGGER_WORDS_ALCO):
        await handle_drink(update, context)
        return

    if any(word in text for word in PUPSIK):
        await handle_pupsik(update, context)
        return

    if any(word in text for word in TRIGGER_WORDS_JOKE):
        await handle_joke(update, context)
        return

